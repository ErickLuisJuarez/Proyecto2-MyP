/**
* Clase para representar un píxel con valores RGB.
*/
public class Pixel {
    public int rojo, verde, azul;
    /**
    * Constructor para inicializar un píxel con valores RGB.
    *
    * @param rojo  Valor de color rojo.
    * @param verde Valor de color verde.
    * @param azul  Valor de color azul.
    */
    public Pixel(int rojo, int verde, int azul) {
      this.rojo = rojo;
      this.verde = verde;
      this.azul = azul;
    }
  }